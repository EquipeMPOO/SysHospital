package aplicacao.dao.util.colecoes;

public class DoencaDePaciente {
	
	private int idDoencaDePaciente;
	private int paciente;
	private int enfermidadepessoal;
	
	public int getIdDoencaDePaciente() {
		return idDoencaDePaciente;
	}
	public void setIdDoencaDePaciente(int idDoencaDePaciente) {
		this.idDoencaDePaciente = idDoencaDePaciente;
	}
	
	public int getPaciente() {
		return paciente;
	}
	public void setPaciente(int paciente) {
		this.paciente = paciente;
	}
	
	public int getEnfermidadepessoal() {
		return enfermidadepessoal;
	}
	public void setEnfermidadepessoal(int enfermidadepessoal) {
		this.enfermidadepessoal = enfermidadepessoal;
	}
}
