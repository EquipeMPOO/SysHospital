package aplicacao.dao.util.colecoes;

public class DoencaDeAtendimento {

	private int idDoencaDeAtendimento;
	private int atendimento;
	private int enfermidadePessoal;
	
	public int getIdDoencaDeAtendimento() {
		return idDoencaDeAtendimento;
	}
	public void setIdDoencaDeAtendimento(int idDoencaDeAtendimento) {
		this.idDoencaDeAtendimento = idDoencaDeAtendimento;
	}
	
	public int getAtendimento() {
		return atendimento;
	}
	public void setAtendimento(int atendimento) {
		this.atendimento = atendimento;
	}
	
	public int getEnfermidadePessoal() {
		return enfermidadePessoal;
	}
	public void setEnfermidadePessoal(int enfermidadePessoal) {
		this.enfermidadePessoal = enfermidadePessoal;
	}
}
