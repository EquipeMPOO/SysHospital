package aplicacao.dao.util.colecoes;

public class SituacaoDePaciente {
	
	private int idSituacaoDePaciente;
	private int entrada;
	private int atendimento;
	
	public int getIdSituacaoDePaciente() {
		return idSituacaoDePaciente;
	}
	public void setIdSituacaoDePaciente(int idSituacaoDePaciente) {
		this.idSituacaoDePaciente = idSituacaoDePaciente;
	}
	
	public int getEntrada() {
		return entrada;
	}
	public void setEntrada(int entrada) {
		this.entrada = entrada;
	}
	
	public int getAtendimento() {
		return atendimento;
	}
	public void setAtendimento(int atendimento) {
		this.atendimento = atendimento;
	}
}
